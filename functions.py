
num1 = int(input("Enter number 1"))
num2 = int(input("Enter number 2"))

def sum_fun(num1, num2):
        return num1 + num2

def sub_fun(num1, num2):
        return num1 - num2

def multipl_fun(num1, num2):
        return num1 * num2

def div_fun(num1, num2):
        return num1 / num2

print(sum_fun(num1, num2))
print(sub_fun(num1, num2))
print(multipl_fun(num1, num2))
print(div_fun(num1, num2))



